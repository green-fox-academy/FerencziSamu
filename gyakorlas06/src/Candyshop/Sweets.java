package Candyshop;

public class Sweets {
  private int price;
  private int sugar;

  public Sweets() {
  }

  public int getPrice() {
    return price;
  }

  public void setPrice(int price) {
    this.price = price;
  }

  public int getSugar() {
    return sugar;
  }

  public void setSugar(int sugar) {
    this.sugar = sugar;
  }
}
